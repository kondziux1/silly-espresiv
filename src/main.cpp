uint8 curimg = 0;
void loop(){

	if (!digitalRead(picturemode))
	{
		if (curimg > 5)
		{
			curimg = 0;
		}

		switch (curimg)
		{
		case 0:
			show();
			break;
		case 1:
			display.clearDisplay();
			display.drawBitmap(0, 0, epd_bitmap_1, 128, 64, WHITE);
			display.display();
			break;
		case 2:
			display.clearDisplay();
			display.drawBitmap(0, 0, epd_bitmap_2, 128, 64, WHITE);
			display.display();
			break;
		case 3:
			display.clearDisplay();
			display.drawBitmap(0, 0, epd_bitmap_3, 128, 64, WHITE);
			display.display();
			break;
		case 4:
			display.clearDisplay();
			display.drawBitmap(0, 0, epd_bitmap_4, 128, 64, WHITE);
			display.display();
			break;
		case 5:
			display.clearDisplay();
			display.drawBitmap(0, 0, epd_bitmap_5, 128, 64, WHITE);
			display.display();
			break;

		default:
			break;
		}
		curimg++;
		delay(150);
	}

	if (!digitalRead(CONFIRMbtn))
	{
		confirm();
		delay(150);
	}
	if (!digitalRead(DELETEbtn))
	{
		deletestuf();
		delay(150);
	}
	if (!digitalRead(UPbtn))
	{
		up();
		delay(150);
	}
	if (!digitalRead(DOWNbtn))
	{
		down();
		delay(150);
	}

	
}

int deletestuf()
{
	display.clearDisplay();
	if (buff.length() > 0)
	{
		buff = buff.substring(0, buff.length() - 1);
	}
	display.setCursor(0, 0);
	display.print(buff);
	display.display();
	display.setCursor(0, 0);
	
	return 0;
}

